public class ForEachLoop {
	public static void main(String[] args)
	{
		int[] arr = {12,43,54,656};
		String[] arr1 = {"one","two","three"};
		for(int a:arr)
		{
			System.out.println(a);
		}
		System.out.println("");
		for(String arr11:arr1)
		{
			System.out.println(arr11);
		}
	}
	
}
